public class Score{
	private String name;
	private String grade;
	private int kor;
	private int eng;
	private int math;

public void setName(String name){
this.name=name;
}
public String getName(){
return name;
}
public void setGrade(String grade){
this.grade=grade;
}
public String getGrade(){
return grade;
}
public void setKor(int kor){
this.kor=kor;
}
public int getKor(){
return kor;
}
public void setEng(int eng){
this.eng=eng;
}
public int getEng(){
return eng;
}
public void setMath(int math){
this.math=math;
}
public int getMath(){
return math;
}
}